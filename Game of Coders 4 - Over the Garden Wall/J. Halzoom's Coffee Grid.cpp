auto Solve(const int &n) -> void {
    int m, k;
    cin >> m >> k;

    vector<vector<int> > grid(n + 1, vector<int>(m + 1)), prefix(n + 1, vector<int>(m + 1, 0));

    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            cin >> grid[i][j];
        }
    }

    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= m; j++) {
            prefix[i][j] = grid[i - 1][j - 1] + prefix[i - 1][j] + prefix[i][j - 1] - prefix[i - 1][j - 1];
        }
    }

    vector<vector<int> > ans(n + 1, vector<int>(m + 1, 0));

    for (int i1 = 0; i1 < n; i1++) {
        for (int j1 = 0; j1 < m; j1++) {
            for (int i2 = i1; i2 < n; i2++) {
                for (int j2 = j1; j2 < m; j2++) {
                    int sum = prefix[i2 + 1][j2 + 1] - prefix[i1][j2 + 1] - prefix[i2 + 1][j1] + prefix[i1][j1];

                    if (sum == k) {
                        ans[i1][j1]++;
                        ans[i1][j2 + 1]--;
                        ans[i2 + 1][j1]--;
                        ans[i2 + 1][j2 + 1]++;
                    }
                }
            }
        }
    }

    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            if (i > 0) ans[i][j] += ans[i - 1][j];
            if (j > 0) ans[i][j] += ans[i][j - 1];
            if (i > 0 && j > 0) ans[i][j] -= ans[i - 1][j - 1];

            cout << (ans[i][j] > 0 ? grid[i][j] : 0);
            if (j < m - 1) cout << " ";
        }
        cout << "\n";
    }
}


